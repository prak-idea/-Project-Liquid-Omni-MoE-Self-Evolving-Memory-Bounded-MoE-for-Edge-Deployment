    def prepare_for_distributed(self):
        """Prepare model for distributed training"""
        if torch.cuda.device_count() < 2:
            print("⚠️ Only one GPU available, using single GPU mode")
            return self.model

        # Split experts across devices
        experts_per_device = len(self.model.experts) // len(self.devices)
        distributed_experts = {}

        for i, device in enumerate(self.devices):
            start_idx = i * experts_per_device
            end_idx = start_idx + experts_per_device if i < len(self.devices) - 1 else len(self.model.experts)

            # Move experts to device
            device_experts = nn.ModuleList([
                expert.to(device)
                for expert in self.model.experts[start_idx:end_idx]
            ])
            distributed_experts[device] = device_experts

        return distributed_experts

    def distributed_forward(self, x):
        """Forward pass with expert parallelism"""
        batch_size, seq_len, dim = x.shape
        outputs = torch.zeros_like(x)

        # Split tokens across devices
        tokens_per_device = batch_size // len(self.devices)

        for i, device in enumerate(self.devices):
            start_idx = i * tokens_per_device
            end_idx = start_idx + tokens_per_device if i < len(self.devices) - 1 else batch_size

            # Get tokens for this device
            device_tokens = x[start_idx:end_idx].to(device)

            # Process with local experts
            device_output = self.model(device_tokens)[0]
            outputs[start_idx:end_idx] = device_output.cpu()

        return outputs

