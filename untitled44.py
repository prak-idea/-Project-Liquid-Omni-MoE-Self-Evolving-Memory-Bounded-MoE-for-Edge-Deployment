class MoEAnalyzer:
    """Comprehensive MoE analysis"""

    def __init__(self, model):
        self.model = model
        self.expert_weights = model.get_expert_weights()
        self.router_weights = model.get_router_weights()
        self.num_experts = len(self.expert_weights)

        print(f"\n📊 Expert Analysis:")
        print(f"   Number of experts: {self.num_experts}")
        print(f"   Expert weight shapes: {self.expert_weights[0].shape}")
        print(f"   Router weight shape: {self.router_weights.shape}")

    def compute_similarity_matrix(self):
        """Compute cosine similarity between all experts"""
        n = self.num_experts
        sim_matrix = np.zeros((n, n))

        for i in range(n):
            for j in range(n):
                a = self.expert_weights[i].flatten()
                b = self.expert_weights[j].flatten()

                # Use subset for speed
                if len(a) > 5000:
                    idx = np.random.choice(len(a), 5000, replace=False)
                    a = a[idx]
                    b = b[idx]

                sim_matrix[i, j] = np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b) + 1e-8)

        return sim_matrix

    def compute_diversity_score(self):
        """Measure expert diversity"""
        sim_matrix = self.compute_similarity_matrix()
        off_diag = sim_matrix[~np.eye(self.num_experts, dtype=bool)]
        diversity = 1 - np.mean(off_diag)
        return diversity

    def analyze_load_balance(self, num_samples=500):
        """Analyze router load distribution"""
        tokens = torch.randn(num_samples, 512)
        if torch.cuda.is_available():
            tokens = tokens.cuda()

        with torch.no_grad():
            _, probs, _ = self.model(tokens.unsqueeze(1))
            probs = probs.squeeze(1).cpu().numpy()

        expert_usage = probs.mean(axis=0)
        balance_score = 1 - (expert_usage.std() / (expert_usage.mean() + 1e-8))

        return expert_usage, balance_score

