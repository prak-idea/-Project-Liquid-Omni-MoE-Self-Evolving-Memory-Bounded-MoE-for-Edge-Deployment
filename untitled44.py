class EigenRouter(nn.Module):
    """Eigenbasis-Guided Routing with load balancing"""

    def __init__(self, input_dim, num_experts, num_eigen=4):
        super().__init__()
        self.num_experts = num_experts
        self.num_eigen = min(num_eigen, input_dim)

        # Learnable eigenbasis
        self.eigen_basis = nn.Parameter(
            torch.randn(self.num_eigen, input_dim) / np.sqrt(input_dim)
        )
        self.router = nn.Linear(input_dim, num_experts, bias=False)

        # Load balancing
        self.register_buffer('expert_usage', torch.zeros(num_experts))
        self.register_buffer('step_count', torch.zeros(1))

    def forward(self, x):
        # Project onto eigenbasis
        eigen_proj = F.linear(x, self.eigen_basis)
        eigen_weights = torch.sigmoid(eigen_proj)

        # Compute routing
        router_logits = self.router(x)

        # Eigen-guided modulation
        modulation = 1 + eigen_weights.mean(dim=-1, keepdim=True)
        weighted_logits = router_logits * modulation

        # Softmax routing
        probs = F.softmax(weighted_logits, dim=-1)

        # Track usage
        with torch.no_grad():
            usage = probs.mean(dim=(0, 1))
            self.expert_usage = 0.9 * self.expert_usage + 0.1 * usage
            self.step_count += 1

        return weighted_logits, probs

