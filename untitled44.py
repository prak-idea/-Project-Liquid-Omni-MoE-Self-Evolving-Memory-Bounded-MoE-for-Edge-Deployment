class AdvancedRouter(nn.Module):
    """
    Multiple routing strategies for MoE
    - Top-K routing with aux loss
    - Expert capacity control
    - Load balancing with noise
    - Drop token routing
    """

    def __init__(self, input_dim, num_experts, top_k=2, capacity_factor=1.0):
        super().__init__()
        self.num_experts = num_experts
        self.top_k = top_k
        self.capacity_factor = capacity_factor
        self.router = nn.Linear(input_dim, num_experts)
        self.noise_epsilon = 1e-2

        # Load balancing
        self.register_buffer('expert_counts', torch.zeros(num_experts))
        self.register_buffer('total_tokens', torch.zeros(1))

    def forward(self, x, add_noise=False):
        batch_size, seq_len, dim = x.shape
        total_tokens = batch_size * seq_len

        # Router logits
        router_logits = self.router(x)

        # Add noise for exploration
        if add_noise and self.training:
            noise = torch.randn_like(router_logits) * self.noise_epsilon
            router_logits = router_logits + noise

        # Top-k routing
        router_probs = F.softmax(router_logits, dim=-1)
        top_k_probs, top_k_indices = torch.topk(router_probs, self.top_k, dim=-1)
        top_k_probs = top_k_probs / (top_k_probs.sum(dim=-1, keepdim=True) + 1e-8)

        # Track expert usage
        with torch.no_grad():
            for k in range(self.top_k):
                indices = top_k_indices[:, :, k]
                for e in range(self.num_experts):
                    count = (indices == e).sum().float()
                    self.expert_counts[e] += count
            self.total_tokens += total_tokens

        # Expert capacity
        capacity = int(self.capacity_factor * total_tokens / self.num_experts)

        return router_probs, top_k_indices, top_k_probs, capacity

    def get_load_balance_loss(self):
        """Compute auxiliary load balancing loss"""
        if self.total_tokens < 1:
            return torch.tensor(0.0, device=self.expert_counts.device)

        importance = self.expert_counts / (self.total_tokens + 1e-8)
        target = 1.0 / self.num_experts
        loss = F.mse_loss(importance, torch.full_like(importance, target))
        return loss

class ExpertChoiceRouter(nn.Module):
    """
    Expert-Choice routing where experts select tokens
    Each expert processes top-k tokens
    """

    def __init__(self, input_dim, num_experts, expert_capacity=2):
        super().__init__()
        self.num_experts = num_experts
        self.expert_capacity = expert_capacity
        self.router = nn.Linear(input_dim, num_experts)

    def forward(self, x):
        batch_size, seq_len, dim = x.shape
        total_tokens = batch_size * seq_len

        # Compute expert scores for all tokens
        router_logits = self.router(x)
        router_probs = F.softmax(router_logits, dim=-1)

        # For each expert, select top-k tokens
        expert_token_maps = []
        for e in range(self.num_experts):
            # Get scores for this expert
            expert_scores = router_probs[:, :, e]
            flat_scores = expert_scores.view(-1)

            # Select top-k tokens for this expert
            top_scores, top_indices = torch.topk(flat_scores,
                                                  min(self.expert_capacity, total_tokens))

            # Convert flat indices to 2D indices
            token_indices = torch.stack([
                top_indices // seq_len,  # batch index
                top_indices % seq_len    # sequence index
            ], dim=1)

            expert_token_maps.append({
                'indices': token_indices,
                'scores': top_scores,
                'expert_id': e
            })

        return expert_token_maps, router_probs

class AdaptiveRouter(nn.Module):
    """
    Adaptive router that adjusts top-k based on input complexity
    """

    def __init__(self, input_dim, num_experts, max_k=4):
        super().__init__()
        self.num_experts = num_experts
        self.max_k = max_k

        # Learn complexity scorer
        self.complexity_scorer = nn.Sequential(
            nn.Linear(input_dim, 128),
            nn.ReLU(),
            nn.Linear(128, 1),
            nn.Sigmoid()
        )

        self.router = nn.Linear(input_dim, num_experts)

    def forward(self, x):
        batch_size, seq_len, dim = x.shape

        # Estimate complexity for each token
        complexity = self.complexity_scorer(x)  # (batch, seq, 1)

        # Determine top-k based on complexity
        k_values = torch.ceil(1 + complexity * (self.max_k - 1)).int()

        # Router logits
        router_logits = self.router(x)
        router_probs = F.softmax(router_logits, dim=-1)

        # Dynamic top-k
        top_k_probs_list = []
        top_k_indices_list = []

        for b in range(batch_size):
            for s in range(seq_len):
                k = k_values[b, s].item()
                if k > 0:
                    probs = router_probs[b, s]
                    top_k_probs, top_k_indices = torch.topk(probs, min(k, self.num_experts))
                    top_k_probs_list.append(top_k_probs)
                    top_k_indices_list.append(top_k_indices)
                else:
                    # If k=0, use all experts
                    top_k_probs_list.append(router_probs[b, s])
                    top_k_indices_list.append(torch.arange(self.num_experts, device=x.device))

        return top_k_probs_list, top_k_indices_list, k_values

