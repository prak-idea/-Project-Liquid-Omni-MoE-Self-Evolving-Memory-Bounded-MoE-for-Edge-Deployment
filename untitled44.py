class MoEBenchmark:
    """
    Comprehensive benchmarking suite
    """

    def __init__(self, model):
        self.model = model
        self.results = {}

    def benchmark_inference_speed(self, batch_sizes=[1, 8, 32], seq_lengths=[16, 64, 256]):
        """Benchmark inference speed"""
        self.model.eval()

        for batch_size in batch_sizes:
            for seq_len in seq_lengths:
                # Create input
                x = torch.randn(batch_size, seq_len, 512)
                if torch.cuda.is_available():
                    x = x.cuda()

                # Warmup
                for _ in range(10):
                    _ = self.model(x)

                # Benchmark
                torch.cuda.synchronize()
                start_time = time.time()

                for _ in range(100):
                    with torch.no_grad():
                        _ = self.model(x)

                torch.cuda.synchronize()
                elapsed_time = time.time() - start_time

                key = f"batch_{batch_size}_seq_{seq_len}"
                self.results[key] = {
                    'time': elapsed_time / 100,
                    'batch_size': batch_size,
                    'seq_len': seq_len,
                    'throughput': batch_size * seq_len / (elapsed_time / 100)
                }

        return self.results

    def benchmark_memory_usage(self):
        """Benchmark memory usage"""
        memory_usage = {}

        # Model size
        total_params = sum(p.numel() for p in self.model.parameters())
        memory_usage['model_size_mb'] = total_params * 4 / (1024 ** 2)

        # Activation memory
        x = torch.randn(1, 64, 512)
        if torch.cuda.is_available():
            x = x.cuda()

        torch.cuda.reset_peak_memory_stats()
        with torch.no_grad():
            _ = self.model(x)

        memory_usage['peak_memory_mb'] = torch.cuda.max_memory_allocated() / (1024 ** 2)

        return memory_usage

    def benchmark_compression(self, original_model):
        """Benchmark compression effectiveness"""
        compression_metrics = {}

        # Parameter count
        original_params = sum(p.numel() for p in original_model.parameters())
        compressed_params = sum(p.numel() for p in self.model.parameters())

        compression_metrics['params_reduction'] = 1 - (compressed_params / original_params)
        compression_metrics['size_reduction_mb'] = (original_params - compressed_params) * 4 / (1024 ** 2)

        # Expert count
        compression_metrics['expert_reduction'] = 1 - (self.model.num_experts / original_model.num_experts)

        return compression_metrics

    def plot_benchmarks(self):
        """Plot benchmark results"""
        fig, axes = plt.subplots(2, 2, figsize=(15, 10))

        # Inference speed
        ax1 = axes[0, 0]
        batch_sizes = []
        times = []
        for key, result in self.results.items():
            if result['seq_len'] == 64:  # Fixed sequence length
                batch_sizes.append(result['batch_size'])
                times.append(result['time'] * 1000)  # Convert to ms

        ax1.plot(batch_sizes, times, 'o-', linewidth=2, markersize=8)
        ax1.set_title('Inference Time vs Batch Size')
        ax1.set_xlabel('Batch Size')
        ax1.set_ylabel('Time per Batch (ms)')
        ax1.grid(True)

        # Throughput
        ax2 = axes[0, 1]
        throughput = [result['throughput'] for result in self.results.values()]
        ax2.hist(throughput, bins=20, alpha=0.7, color='green')
        ax2.set_title('Throughput Distribution')
        ax2.set_xlabel('Throughput (tokens/sec)')
        ax2.set_ylabel('Frequency')

        # Memory usage
        ax3 = axes[1, 0]
        memory = self.benchmark_memory_usage()
        ax3.bar(list(memory.keys()), list(memory.values()), color=['blue', 'orange'])
        ax3.set_title('Memory Usage')
        ax3.set_ylabel('MB')
        ax3.tick_params(axis='x', rotation=45)

        # Compression metrics
        ax4 = axes[1, 1]
        metrics = self.benchmark_compression(self.model)  # Placeholder
        ax4.pie(list(metrics.values()), labels=list(metrics.keys()), autopct='%1.1f%%')
        ax4.set_title('Compression Metrics')

        plt.tight_layout()
        plt.savefig('benchmark_results.png', dpi=150)
        plt.show()

