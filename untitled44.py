class AdvancedQuantizer:
    """
    Multiple quantization strategies for MoE
    - Post-training quantization
    - Quantization-aware training
    - Mixed precision
    """

    def __init__(self, bits=8, symmetric=True):
        self.bits = bits
        self.symmetric = symmetric
        self.scale = None
        self.zero_point = None

    def quantize_weights(self, weights):
        """Quantize weights to lower precision"""
        if self.symmetric:
            # Symmetric quantization
            max_val = torch.max(torch.abs(weights))
            self.scale = max_val / (2 ** (self.bits - 1) - 1)
            quantized = torch.round(weights / self.scale)
            return quantized.clamp(-2**(self.bits-1), 2**(self.bits-1)-1)
        else:
            # Asymmetric quantization
            min_val = torch.min(weights)
            max_val = torch.max(weights)
            self.scale = (max_val - min_val) / (2 ** self.bits - 1)
            self.zero_point = -torch.round(min_val / self.scale)
            quantized = torch.round(weights / self.scale + self.zero_point)
            return quantized.clamp(0, 2**self.bits - 1)

    def dequantize_weights(self, quantized_weights):
        """Dequantize weights back to original precision"""
        if self.symmetric:
            return quantized_weights * self.scale
        else:
            return (quantized_weights - self.zero_point) * self.scale

    def quantize_experts(self, experts):
        """Quantize all experts in the model"""
        quantized_experts = []
        scales = []
        zero_points = []

        for expert in experts:
            expert_quantized = {}
            for name, param in expert.named_parameters():
                if 'weight' in name:
                    q_weight = self.quantize_weights(param.data)
                    expert_quantized[name] = q_weight
                    scales.append(self.scale)
                    zero_points.append(self.zero_point)
                else:
                    expert_quantized[name] = param.data

            quantized_experts.append(expert_quantized)

        return quantized_experts, scales, zero_points

class MixedPrecisionTrainer:
    """
    Mixed precision training with gradient scaling
    """

    def __init__(self, model, optimizer, scaler=None):
        self.model = model
        self.optimizer = optimizer
        self.scaler = scaler or torch.cuda.amp.GradScaler()

    def train_step(self, inputs, targets):
        """Single training step with mixed precision"""
        self.optimizer.zero_grad()

        # Forward pass with autocast
        with torch.cuda.amp.autocast():
            outputs, _, _ = self.model(inputs)
            loss = F.mse_loss(outputs, targets)

        # Backward pass with gradient scaling
        self.scaler.scale(loss).backward()
        self.scaler.step(self.optimizer)
        self.scaler.update()

        return loss.item()

