class ExpertDiversityRegularizer:
    """
    Regularize experts to maintain diversity
    """

    def __init__(self, diversity_coeff=0.01):
        self.diversity_coeff = diversity_coeff

    def compute_diversity_loss(self, expert_weights):
        """Compute loss to encourage expert diversity"""
        n_experts = len(expert_weights)
        total_loss = 0

        for i in range(n_experts):
            for j in range(i + 1, n_experts):
                # Cosine similarity between experts
                a = expert_weights[i].flatten()
                b = expert_weights[j].flatten()
                similarity = torch.dot(a, b) / (torch.norm(a) * torch.norm(b) + 1e-8)

                # Penalize high similarity
                total_loss += torch.abs(similarity)

        return self.diversity_coeff * total_loss / (n_experts * (n_experts - 1) / 2)

