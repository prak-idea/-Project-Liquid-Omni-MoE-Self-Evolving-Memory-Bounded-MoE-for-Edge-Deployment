class OmniMoEScheduler:
    """Expert-Centric Scheduling with grouped GEMM"""

    def __init__(self, num_experts):
        self.num_experts = num_experts
        self.grouped_tokens = {}

    def schedule(self, tokens, expert_indices):
        """Group tokens by expert assignment"""
        batch_size, seq_len, _ = tokens.shape

        # Initialize groups
        self.grouped_tokens = {i: [] for i in range(self.num_experts)}
        self.grouped_indices = {i: [] for i in range(self.num_experts)}

        # Group tokens
        for b in range(batch_size):
            for s in range(seq_len):
                expert_id = expert_indices[b, s].item()
                self.grouped_tokens[expert_id].append(tokens[b, s])
                self.grouped_indices[expert_id].append((b, s))

        # Convert to tensors
        grouped_tensors = {}
        for expert_id, token_list in self.grouped_tokens.items():
            if token_list:
                grouped_tensors[expert_id] = torch.stack(token_list)
            else:
                grouped_tensors[expert_id] = torch.tensor([], device=tokens.device)

        return grouped_tensors

    def process(self, grouped_tokens, experts):
        """Process grouped tokens through experts"""
        outputs = {}

        for expert_id, tokens in grouped_tokens.items():
            if len(tokens) > 0 and expert_id < len(experts):
                outputs[expert_id] = experts[expert_id](tokens)

        return outputs

    def compute_speedup(self, batch_size, seq_len, num_experts):
        """Estimate speedup from scheduling"""
        naive_ops = batch_size * seq_len * num_experts
        avg_group_size = (batch_size * seq_len) / num_experts
        omnimoe_ops = batch_size * seq_len + num_experts * avg_group_size
        speedup = naive_ops / (omnimoe_ops + 1e-8)
        return speedup

