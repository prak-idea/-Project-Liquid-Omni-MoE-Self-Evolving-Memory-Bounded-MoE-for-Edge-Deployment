class MoEInterpreter:
    """
    Interpret and visualize MoE decisions
    """

    def __init__(self, model, tokenizer=None):
        self.model = model
        self.tokenizer = tokenizer

    def visualize_routing(self, input_tokens, top_k=2):
        """Visualize routing decisions"""
        self.model.eval()

        with torch.no_grad():
            if torch.cuda.is_available():
                input_tokens = input_tokens.cuda()

            _, probs, indices = self.model(input_tokens)
            probs = probs.cpu().numpy()
            indices = indices.cpu().numpy()

        # Create routing heatmap
        fig, axes = plt.subplots(2, 1, figsize=(15, 8))

        # Probability heatmap
        ax1 = axes[0]
        im = ax1.imshow(probs[0, :, :].T, aspect='auto', cmap='hot')
        ax1.set_title('Expert Activation Probabilities')
        ax1.set_xlabel('Token Position')
        ax1.set_ylabel('Expert Index')
        plt.colorbar(im, ax=ax1)

        # Expert assignment
        ax2 = axes[1]
        for k in range(top_k):
            assignments = indices[:, :, k] if len(indices.shape) > 2 else indices
            ax2.scatter(range(assignments.shape[1]), assignments[0],
                       label=f'Top-{k+1}', alpha=0.7, s=50)
        ax2.set_title('Expert Assignments')
        ax2.set_xlabel('Token Position')
        ax2.set_ylabel('Expert Index')
        ax2.legend()

        plt.tight_layout()
        plt.show()

        return probs, indices

    def analyze_expert_behavior(self, dataset):
        """Analyze what each expert learns"""
        expert_behavior = {i: [] for i in range(self.model.num_experts)}

        for sample in dataset:
            with torch.no_grad():
                _, probs, indices = self.model(sample.unsqueeze(0))
                probs = probs.squeeze(0).cpu().numpy()

                for token_idx, probs_token in enumerate(probs):
                    # Get top expert
                    top_expert = np.argmax(probs_token)
                    expert_behavior[top_expert].append(probs_token)

        return expert_behavior

    def compute_attention_patterns(self, input_tokens):
        """Compute expert attention patterns"""
        self.model.eval()

        with torch.no_grad():
            outputs, probs, indices = self.model(input_tokens)

        # Compute expert attention
        attention_patterns = []
        for expert_idx in range(self.model.num_experts):
            mask = (indices == expert_idx).float()
            attention = mask * probs
            attention_patterns.append(attention.mean(dim=(0, 1)).cpu().numpy())

        return np.array(attention_patterns)

