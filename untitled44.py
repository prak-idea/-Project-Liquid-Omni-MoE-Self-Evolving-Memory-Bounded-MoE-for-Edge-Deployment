class SyntheticMoE(nn.Module):
    """Fully functional synthetic MoE model with 8 experts"""

    def __init__(self, input_dim=512, hidden_dim=1024, num_experts=8, top_k=2):
        super().__init__()
        self.num_experts = num_experts
        self.top_k = top_k
        self.input_dim = input_dim

        # Create experts with different specializations
        self.experts = nn.ModuleList([
            nn.Sequential(
                nn.Linear(input_dim, hidden_dim),
                nn.GELU(),
                nn.Linear(hidden_dim, input_dim)
            )
            for _ in range(num_experts)
        ])

        # Router with learnable weights
        self.router = nn.Linear(input_dim, num_experts)

        # Add some specialization to each expert
        with torch.no_grad():
            for i, expert in enumerate(self.experts):
                scale = 0.5 + 0.5 * (i / num_experts)
                for param in expert.parameters():
                    param.data *= scale

        print(f"✅ Created synthetic MoE with {num_experts} experts")
        print(f"   Input dim: {input_dim}, Hidden dim: {hidden_dim}")

    def forward(self, x):
        # Routing
        router_logits = self.router(x)
        router_probs = F.softmax(router_logits, dim=-1)

        # Top-k routing
        top_k_probs, top_k_indices = torch.topk(router_probs, self.top_k, dim=-1)
        top_k_probs = top_k_probs / top_k_probs.sum(dim=-1, keepdim=True)

        # Process through experts
        output = torch.zeros_like(x)
        for k in range(self.top_k):
            expert_idx = top_k_indices[:, :, k]
            expert_weight = top_k_probs[:, :, k].unsqueeze(-1)

            for e in range(self.num_experts):
                mask = (expert_idx == e).unsqueeze(-1).float()
                expert_out = self.experts[e](x)
                output += mask * expert_weight * expert_out

        return output, router_probs, top_k_indices

    def get_expert_weights(self):
        """Extract weights from all experts"""
        weights = []
        for expert in self.experts:
            # Get first linear layer weights
            for module in expert:
                if isinstance(module, nn.Linear):
                    weights.append(module.weight.detach().cpu().numpy())
                    break
        return np.array(weights)

    def get_router_weights(self):
        """Extract router weights"""
        return self.router.weight.detach().cpu().numpy()

