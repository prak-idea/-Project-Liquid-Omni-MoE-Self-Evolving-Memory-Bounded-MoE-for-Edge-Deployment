class HCSMoECompressor:
    """Hierarchical Clustering Sparse MoE"""

    def __init__(self, strategy='ward', n_clusters=None):
        self.strategy = strategy
        self.n_clusters = n_clusters
        self.labels = None

    def compress(self, expert_weights, similarity_matrix, target_ratio=0.5):
        """Compress experts using hierarchical clustering"""
        print(f"\n🔄 HC-SMoE Compression (target: {target_ratio:.1%})")

        n_experts = len(expert_weights)

        if self.n_clusters is None:
            self.n_clusters = max(2, int(n_experts * target_ratio))

        # Flatten experts
        X = np.array([w.flatten() for w in expert_weights])

        # Reduce dimensionality
        n_components = min(50, X.shape[0]-1, X.shape[1])
        if n_components > 1:
            pca = PCA(n_components=n_components)
            X_reduced = pca.fit_transform(X)
        else:
            X_reduced = X

        # Hierarchical clustering
        if len(X_reduced) > self.n_clusters:
            clustering = AgglomerativeClustering(
                n_clusters=self.n_clusters,
                linkage=self.strategy
            )
            self.labels = clustering.fit_predict(X_reduced)
        else:
            self.labels = np.arange(len(X_reduced))

        # Merge experts within each cluster
        compressed = []
        compression_info = []

        for cluster_id in range(max(self.labels) + 1):
            cluster_mask = self.labels == cluster_id
            cluster_indices = np.where(cluster_mask)[0]

            if len(cluster_indices) > 0:
                cluster_weights = [expert_weights[i] for i in cluster_indices]
                weights = [similarity_matrix[i, cluster_indices].mean() for i in cluster_indices]
                weights = np.array(weights)
                weights = weights / (weights.sum() + 1e-8)

                merged = np.zeros_like(cluster_weights[0])
                for w, weight in zip(cluster_weights, weights):
                    merged += weight * w

                compressed.append(merged)
                compression_info.append({
                    'cluster_id': cluster_id,
                    'original_experts': cluster_indices.tolist(),
                    'size': len(cluster_indices)
                })

        print(f"✅ Compressed {n_experts} → {len(compressed)} experts")
        print(f"   Compression ratio: {len(compressed)/n_experts:.2%}")

        return np.array(compressed), compression_info

