class DERNRecombiner:
    """Dynamic Expert Recombination with Neurons"""

    def __init__(self, recombination_rate=0.3):
        self.rate = recombination_rate

    def recombine(self, expert_weights, similarity_matrix):
        """Recombine neurons across compatible experts"""
        print(f"\n🔄 DERN Recombination (rate: {self.rate:.1%})")

        n_experts = len(expert_weights)
        recombined = []
        recombination_log = []

        for i in range(n_experts):
            expert = expert_weights[i].copy()

            if i < len(similarity_matrix):
                compat = similarity_matrix[i]
                compat[i] = -1
                top_indices = np.argsort(compat)[::-1][:2]

                for j in top_indices:
                    if j < len(expert_weights):
                        n_neurons = expert.shape[0]
                        swap_count = max(1, int(self.rate * n_neurons))
                        swap_indices = np.random.choice(
                            n_neurons,
                            min(swap_count, n_neurons),
                            replace=False
                        )

                        for idx in swap_indices:
                            expert[idx] = 0.7 * expert[idx] + 0.3 * expert_weights[j][idx]

                        recombination_log.append({
                            'expert': i,
                            'donor': j,
                            'neurons_recombined': len(swap_indices)
                        })

            recombined.append(expert)

        print(f"✅ Recombined {len(recombined)} experts")
        print(f"   Total recombinations: {len(recombination_log)}")

        return np.array(recombined), recombination_log

